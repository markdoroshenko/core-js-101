function roundToPowerOfTen(num, pow) {
  return Math.round(num / (10 ** pow)) * (10 ** pow);
}

function isPrime(n) {
  if (n < 2) {
    return false;
  }
  const nums = [2, 3, 5, 7];
  // eslint-disable-next-line no-restricted-syntax
  for (const number of nums) {
    if (n === number) {
      return true;
    }
    if (n % number === 0) {
      return false;
    }
  }
  return true;
}

